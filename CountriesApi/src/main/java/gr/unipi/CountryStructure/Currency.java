package gr.unipi.CountryStructure;

public class Currency {
    private String name;
    private String symbol;

    public Currency() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name2) {
        name = name2;
    }

    public String getSymbol() {
        return symbol;
    }

    public void setSymbol(String symbol) {
        this.symbol = symbol;
    }
}